#include "libft.h"
#include <stdio.h>

int	ft_strneg(const char *s)
{
	if (*s == '-')
	{
		s++;
		while (*s)
		{
			if (!ft_isdigit(*s))
				return (0);
			s++;
		}
		return (1);
	}
	return (0);
}
